package castingUse;

public class ImplicitiandExplicit {

	public static void main(String[] args) {
		int a=999;
		System.out.println(a);
		double b=a;
		System.out.println(b);
		System.out.println("===================");
		//explicit casting
		double d= 199.99d;
		System.out.println(d);
		int e=(int) d;
		System.out.println(e);

	}

}
