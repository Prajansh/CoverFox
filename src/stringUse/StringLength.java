package stringUse;

public class StringLength {

	public static void main(String[] args) {
	String s="Pune";
	String mail="121@gmail.com";
	String count=new String("5 6 7 8 9");
	String name=new String("Ashu...");
	
	
	System.out.println("Pune length:- "+s.length());
	System.out.println("Mail length:- " +mail.length());
	System.out.println("Count length:- " +count.length());
	System.out.println("Name length:- "+name.length());
	
	

	}

}
